    private void jLabel8MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MousePressed
        // TODO add your handling code here:
        String ay = jAyComboBox.getSelectedItem().toString();
        String gün = jGünComboBox.getSelectedItem().toString();
        if(ay.equals("0")&&gün.equals("0")){
            javax.swing.JOptionPane.showMessageDialog(null,"LUTFEN VADE SURESİNİ AYARLAYINIZ!");
        }
        else{
            int toplamGün = (Integer.valueOf(ay)*30)+ Integer.valueOf(gün);
            double vadeSonuTutar = Double.valueOf(jVadeSonuText.getText());
            ((Musteri)currentUser).vadeliHesapInit(vadeliHesapNo, vadeliPara, vadeSonuTutar, toplamGün);
            this.setVisible(false);
            this.dispose();
            javax.swing.JOptionPane.showMessageDialog(null,"ISLEMINIZ BASARILI BIR SEKILDE GERCEKLESTI\nHER PROGRAM ACILDIGINDA BIR GUN GECER!!");
            VadeliFrame.mainVadeliFrame(currentUser);
        }
    }//GEN-LAST:event_jLabel8MousePressed

