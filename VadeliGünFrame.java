    private void jAyComboBoxPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_jAyComboBoxPopupMenuWillBecomeInvisible
        // TODO add your handling code here:
        String ay = jAyComboBox.getSelectedItem().toString();
        String gün = jGünComboBox.getSelectedItem().toString();
        if(ay.equals("0") && gün.equals("0")){
            jVadeSonuText.setText("");
        }
        else{
            int _ay = Integer.valueOf(ay);
            int _gün = Integer.valueOf(gün);
            double vadeSonuTutar = vadeliPara + ((vadeliPara*10) * (((30*_ay)+_gün)/(double)360));
            vadeSonuTutar = (double) java.lang.Math.round(vadeSonuTutar*10)/10;
            jVadeSonuText.setText(String.valueOf(vadeSonuTutar));
        }
    }//GEN-LAST:event_jAyComboBoxPopupMenuWillBecomeInvisible

