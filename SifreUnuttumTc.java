    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
        int rValue = User.checkUyari(jTextField1.getText(),11);
        if(rValue==1) jUyari.setText("11 HANE VE RAKAM OLMALI");
        else if(rValue==2)jUyari.setText("11 HANE OLMAMLI");
        else if(rValue==3)jUyari.setText("SADECE RAKAM İCERMELİ");
        else jUyari.setText("");
    }//GEN-LAST:event_jTextField1KeyReleased

    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        // TODO add your handling code here:
        if(jTextField1.getText().equals(""))javax.swing.JOptionPane.showMessageDialog(null,"ALANI BOS BIRAKMAYINIZ!");
        else if(!(jUyari.getText().equals(""))) javax.swing.JOptionPane.showMessageDialog(null, "TC, 11 HANE VE SADECE RAKAM OLMALI");
        else{
            User rUser = User.tcVarMi(jTextField1.getText(),flag);
            if(rUser==null){
                javax.swing.JOptionPane.showMessageDialog(null,"BU TC NOLU KULLANICI YOK!");
            }
            else{
                this.setVisible(false);
                this.dispose();
                MenuSifreDegis.mainMenuSifreDegis(rUser,flag);
            }
        }
    }//GEN-LAST:event_jButton1MousePressed

