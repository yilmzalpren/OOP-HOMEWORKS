    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String onay = String.valueOf(OnayKoduGir.onayKodu);
        if(onay.equals(jTextField1.getText())){
            java.io.BufferedWriter writeToFile = null ; 
            java.io.BufferedReader readFromFile = null ; 
            String keeper = null ; 
             
            try{
                
                readFromFile = new java.io.BufferedReader(new java.io.FileReader("src/Files/user.txt"));
                keeper = readFromFile.readLine();
                readFromFile.close();
                writeToFile = new java.io.BufferedWriter(new java.io.FileWriter("src/Files/user.txt",true));
                String spe ; 
                if(flag==1){
                    spe = "m//";
                }
                else if(flag==2){
                    spe = "p//";
                }
                else {
                    spe = "y//";
                }
                writeToFile.write(spe+yeniUser.getTc()+"//"+yeniUser.getSifre()+"//"+yeniUser.getAd()+"//"
                        +yeniUser.getSoyad()+"//"+yeniUser.getCinsiyet()+"//"+yeniUser.getDate().day+"."+yeniUser.getDate().month+"."
                        +yeniUser.getDate().year+"//"+yeniUser.getTelNo());
                writeToFile.newLine();
                
                writeToFile.close();
                this.setVisible(false);
                this.dispose();
                if(flag==1)
                    MusteriLogin.mainMusteriLogin();
                else PersonelOrYönetici.mainPersonelOrYönetici();
                
            }catch(java.io.IOException e){
                javax.swing.JOptionPane.showMessageDialog(null,"DOSYA YOLU BULUNAMIYOR\nIDE ile calistirirsaniz calisacaktir!");
            }
        }
        else{
            counter--;
            javax.swing.JOptionPane.showMessageDialog(null, "ONAY KODUNUZ YANLIŞ!\nTEKRAR DENEYİNİZ");
            if(counter==0){
                javax.swing.JOptionPane.showMessageDialog(null,"COK FAZLA HATALİ GİRİS YAPTİNİZ");
                System.exit(1);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

