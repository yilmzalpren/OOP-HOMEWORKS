    private void jYatirimTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jYatirimTableMousePressed
        // TODO add your handling code here:
        int index = jYatirimTable.getSelectedRow();
        if(index!=-1){
           String hesapNo = jYatirimTable.getValueAt(index,0).toString();
           String bakiye = jYatirimTable.getValueAt(index,1).toString();
           String diger = jYatirimTable.getValueAt(index, 2).toString();
           
           if(bakiye.equals("0.0")&&diger.equals("0.0")){
               // İLK OLUSTURMA OLDUGU İCİN DİREK BAKİYENİN SECİLECEGİ VADESİZ HESAP SORULMALI YA DA 
               // VADESİZ HESAP YOK İSE ÖNCE VADESİZ HESAP OLUSTURUP ONA PARA YÜKLEYİN DENİLMELİ!
               if(!(((Musteri)currentUser).vadesizHesapVarMi())){
                   this.setVisible(false);
                   this.dispose();
                   VadesizFrame.mainVadesizFrame(currentUser);
                   javax.swing.JOptionPane.showMessageDialog(null,"YATİRİM HESABA PARA YATIRMA İSLEMLERİ\nVADESİZ HESAP İLE OLMAKTADIR!\nLUTFEN"
                           + " ONCE VADESİZ HESAP OLUSTURUP BU ISLEMİ OYLE YAPINIZ!");
               }
               else{
                   this.setVisible(false);
                   this.dispose();
                   // YATİRİM HESABINA HANGİ VADESİZ HESAPTAN PARA AKTARMAK İSTEDİGİNİ SORACAGIZ
                  YatirimIcınVadesizHesap.mainYatırımIcınVadesizHesap(currentUser, hesapNo, flag);
               }
               
           }
           else{
               // ILK OLUSTURULMA DEGIL O ZAMAN FARKLI FRAME ACACAGIZ
               this.setVisible(false);
               this.dispose();
               YatirimSpesifik.mainYatirimSpesifik(currentUser, hesapNo, bakiye, diger, flag);
               
           }
        }
    }//GEN-LAST:event_jYatirimTableMousePressed

