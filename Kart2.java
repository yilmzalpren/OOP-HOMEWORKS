    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
        // TODO add your handling code here:
        if(((Musteri)currentUser).istekListesindeVarMi(hesapNo)){
            javax.swing.JOptionPane.showMessageDialog(null,"DAHA ONCE LIMIT DEGISIM ISTEGINDE BULUNDUGUNUZ ICIN\nYENİ BİR ISTEK"
                    + " YAPAMIYORSUNUZ");
            return  ; 
        }
        String yeniLimit ; 
        yeniLimit = javax.swing.JOptionPane.showInputDialog(null,"ISTEDIGINIZ LIMITI GIRIN");
        try{
            Integer.valueOf(yeniLimit);
        }catch(NumberFormatException e1){
            javax.swing.JOptionPane.showMessageDialog(null,"LUTFEN UYGUN INPUT GİRİNİZ\nSADECE TAM SAYI LIMITLER GECERLIDIR!");
            return ; 
        }
        // DEMEKKI FORMAT UYGUN
        String mesaj ;
        mesaj = javax.swing.JOptionPane.showInputDialog(null,"NEDEN YENİ LİMİT İSTEDİGİNİZİ KISACA ACIKLAR MIISNIZ?");
        int size = mesaj.length() ;
        if(size==0){
            javax.swing.JOptionPane.showMessageDialog(null,"LUTFEN NEDENİZİ BELİRTİN");
            return ;
        }
        for(int i = 0 ; i  < size ; ++i) {
            if(mesaj.charAt(0)=='/') {
                javax.swing.JOptionPane.showMessageDialog(null,"GECERSIZ INPUT");
                return ; 
            }
        }
        //---------------- BURAYA GELMIS ISE ARTIK LIMITISTEK.TXT'E YAZMAM LAZIM
        ((Musteri)currentUser).yeniLimitIstek(hesapNo,limit,yeniLimit,mesaj);
        javax.swing.JOptionPane.showMessageDialog(null,"ISTEGINIZ BIZE ILETILMISTIR\nYETKILILERIMIZIN DONMESINI BEKLEYİN");
        this.setVisible(false);
        this.dispose();
        MusteriMenu.mainMusteriMenu(currentUser);
    }//GEN-LAST:event_jButton1MousePressed

