    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if(index!=-1){
           String tc = jTable1.getValueAt(index, 2).toString();
           if(!(Musteri.tcVadesizVarMi(tc))){
               javax.swing.JOptionPane.showMessageDialog(null,"GONDERMEK İSTEDGİNİNZ SAHSIN VADESİZ HESABI YOK\nPARA TRANSFERLERİ VADESİZ"
                       + " HESAPLAR ILE OLMAKTADIR");
           }
           else{
               javax.swing.table.DefaultTableModel table2 = (javax.swing.table.DefaultTableModel)jTable2.getModel();
               table2.setRowCount(0);
               java.util.ArrayList<String[]> rObj = Musteri.vadesizHesaplariDondur(tc);
               int size = rObj.size();
               for(int i = 0 ; i  < size ; ++i){
                 
                   table2.addRow(rObj.get(i));

               }
           }
        }
    }//GEN-LAST:event_jTable1MousePressed

